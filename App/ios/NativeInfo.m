#import <Foundation/Foundation.h>
#import <React/RCTBridgeModule.h>

@interface NativeInfo : NSObject <RCTBridgeModule>

@end

@implementation NativeInfo

RCT_EXPORT_MODULE();

- (NSString *)_detectImagesPath
{
#if TARGET_IPHONE_SIMULATOR && DEBUG
    return @SRCROOT "/../src/autogenerated/images_dir/";
#else
    return [[NSBundle mainBundle].resourcePath stringByAppendingString:@"/assets/src/autogenerated/images_dir/"];
#endif
}

- (NSDictionary *)constantsToExport
{
    return @{
             @"imagesPath": [self _detectImagesPath]
             };
}

@end
